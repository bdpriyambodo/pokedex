func PokeApiRaw(client *http.Client, fullUrl string) ([]byte, error) {

	// fullUrl := url + fmt.Sprintf("?offset=%d&limit=20", offset)

	res, err := client.Get(fullUrl)
	if err != nil {
		log.Fatal(err)
	}

	// fmt.Println("res:", res)
	// fmt.Println("err:", err)
	// fmt.Println("===============================")

	body, err := io.ReadAll(res.Body)
	res.Body.Close()

	if err != nil {
		log.Fatal(err)
	}

	return body, err

	// fmt.Println("body:", string(body))
	// fmt.Println("err:", err)
	// fmt.Println("===============================")

}

